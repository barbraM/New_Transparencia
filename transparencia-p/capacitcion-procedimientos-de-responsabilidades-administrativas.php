							<p style="text-align: justify;">Bajo la coordinación de la Secretaría de la Contraloría General del Gobierno del Estado y con el apoyo del Tribunal de Justicia Administrativa de Sonora se llevó a cabo esta instrucción para precisar los distintos conceptos y actividades que deben realizarse para revisar y, en su caso, sancionar las faltas administrativas en
							las que puedan incurrir los servidores públicos.</p>	
							<p style="text-align: justify;">Entre éstas se señalaron las correspondientes a las no graves, como no denunciar actos indebidos u omisiones, no atender instrucciones superiores o no rendir
							cuentas del ejercicio de funciones; así como las relativas a las graves, entre las que se encuentran el cohecho, el peculado, el desvío de recursos públicos, la utilización indebida de información, el abuso de funciones, la actuación bajo conflicto de interés, el tráfico de influencias, el desacato y el enriquecimiento oculto.</p>
							<p style="text-align: justify;">De igual forma, se presentaron las faltas graves en las que pueden verse vinculados y sujetos a sanción los particulares que tomen parte en ellas, como soborno, participación ilícita en procedimientos administrativos, utilización de
							información falsa, colusión, uso indebido de recursos públicos, contratación
							indebida de ex servidores públicos y demás, lo cual representa un aspecto
							novedoso en la ley de la materia.</p>
							<p style="text-align: justify;">Durante la sesión de capacitación fueron explicadas cada una de las etapas del procedimiento, como la investigación, donde se establece cómo ocurrieron los hechos y si de ellos se presume la existencia de una falta; la substanciación, en la cual se admite el Informe de presunta responsabilidad y se desarrollan las audiencias, desahogo de pruebas y alegatos; terminando con la resolución, donde se emite la sentencia por la falta cometida con la sanción que corresponde a la magnitud de la misma.</p>
							<p style="text-align: justify;">Se precisaron también los plazos en que deben desarrollarse cada una de ellas, así como las autoridades que pueden participar, dejando en claro que en el caso de las faltas administrativas graves y de particulares, la única autoridad resolutora
							será el Tribunal de Justicia Administrativa a través de la Sala Especializada en Materia de Anticorrupción y Responsabilidades Administrativas.</p>
							<p style="text-align: justify;">La información recibida permitirá a los participantes llevar a cabo de forma oportuna y eficiente sus respectivos procedimientos de responsabilidad administrativa, a fin de evitar caer ellos mismos en situación de falta al no aplicarlos cuando sea procedente, de acuerdo a la normatividad.</p>
							<p style="text-align: justify;">Esto cobra especial relevancia en el caso de las contralorías municipales, pues ante el próximo proceso de entrega recepción de los ayuntamientos es necesario que inicien y se incluyan en él los procedimientos que correspondan, para un adecuado deslinde de responsabilidades.</p>
							<p style="text-align: justify;">Ante tal circunstancia, y atendiendo solicitud de los servidores públicos municipales, esta capacitación vino a reforzar las sesiones que sobre el tema se brindaron a personal de los órganos de control y evaluación gubernamental durante las pasadas reuniones regionales de la Comisión Permanente de Contralores Estado - Municipios.</p>

