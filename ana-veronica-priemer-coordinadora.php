							<p style="text-align: justify;">Este miércoles se llevó a cabo la instalación y toma de protesta del primer comité directivo de Agencia Municipal Anticorrupción Caere Municipio Virtual por la Sustentabilidad, proyecto estudiantil encabezado por Ana Verónica Castro Luna, alumna del octavo semestre de la Licenciatura en Administración Pública de la Universidad de Sonora.</p>	
							<p style="text-align: justify;">La toma de protesta se llevó a cabo ante la presencia de Ricardo García Sánchez, magistrado presidente de la Sala Especializada en Materia Anticorrupción y Responsabilidades Administrativas del Tribunal de Justicia de Sonora, así como del académico Aarón Grajeda Bustamante.</p>
							<p style="text-align: justify;">Castro Luna expresó que es un orgullo representar a la Agencia en el proyecto Caere, especialmente en el contexto de un tema vigente de grave impacto en la sociedad, como es la corrupción, que deberá enfrentarse de manera conjunta en aras de fortalecer a los municipios para el bienestar de sus pobladores, sostuvo.</p>
							<p style="text-align: justify;">Acompañada por Paulina Alejandra Robles Romero, secretaria, así como Alexandra Iribe Gutiérrez, Melissa María Gallegos Soto, Wilfrido Yeomans Orozco, Ana Melissa Aguirre Loreto y Javier Gorosave Zúñiga, vocales, dijo que resulta fundamental que el gobierno municipal cuente con las herramientas de prevención y control de la corrupción que eviten que este tipo de actos afecten negativamente los objetivos, las decisiones institucionales y, por consiguiente, mermen su credibilidad y confianza ciudadana.</p>
							<p style="text-align: justify;">"Sin duda, urge ofrecer información precisa sobre la forma en que tiene lugar ese mal en cada una de las estructuras administrativas municipales", dijo en la ceremonia también presidida por Paulina Ocaña Encinas, presidenta de Caere Municipio Virtual, y el académico Armando Andrade Márquez, de quien dijo que desde 1994 a la fecha promueve desde esta casa de estudios una cultura municipalista.</p>
							<p style="text-align: justify;">Ana Verónica Castro destacó que señaló que la información contenida en el Mapa Institucional de Riesgos de Corrupción Municipal sirve luego para definir estrategias de prevención, control y erradicación de actos de corrupción, contando con el apoyo de los altos mandos gubernamentales, la participación directa de los servidores públicos y la colaboración de los ciudadanos y sus organizaciones sociales.</p>
							<p style="text-align: justify;">Dio a conocer que sus principales líneas estratégicas son el fortalecimiento de capacidades institucionales (Diagnóstico, prevención y corrección); la prevención social de la corrupción (denuncia ciudadana, contraloría social); corrección y sanción de comportamientos indebidos (Responsabilidades, sanciones y denuncias) y participación ciudadana (Campañas de educación y sensibilización a ciudadanos, ONG, sindicatos y universidades).</p>
							<p style="text-align: justify;">Advirtió que como parte del Sistema Estatal Anticorrupción, por conducto de los Órganos de Control y Evaluación Gubernamental, deberán tomarse las medidas necesarias para la prevención, detección y sanción de responsabilidades administrativas y hechos de corrupción en el ámbito de su competencia.</p>
							<p style="text-align: justify;">Hizo referencia a que fue el pasado 30 de mayo cuando se aprobó la creación de la Agencia –primera en la entidad y la región noroeste de México– como un ejercicio académico donde podrán recrearse distintos escenarios de actividad municipal que les permita ofrecer servicios de apoyo, asesoría y asistencia técnica a gobiernos municipales del estado de Sonora, en temas específicos que la propia circunstancia demanda.</p>
							<p style="text-align: justify;">Recordó que Caere es un proyecto de la Universidad de Sonora que se desarrolla a través de la Academia de Estudios Municipales del Departamento de Sociología y Administración Pública, cuyo objetivo general es capacitar a los alumnos acerca del origen, administración y operación de un ayuntamiento, aglutinando a estudiantes de todas las carreras de la institución.</p>
							<p style="text-align: justify;">
								Por otra parte, brindó un reconocimiento a la Universidad de Sonora por permitirles el desarrollo de ese proyecto, el cual indicó que después de diez años se mantiene vigente y a la vanguardia a nivel estatal y nacional. 
							</p>
